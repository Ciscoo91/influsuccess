import axios from 'axios';

import { ISocialNetworkLink } from '@/shared/model/social-network-link.model';

const baseApiUrl = 'api/social-network-links';

export default class SocialNetworkLinkService {
  public find(id: number): Promise<ISocialNetworkLink> {
    return new Promise<ISocialNetworkLink>((resolve, reject) => {
      axios
        .get(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public retrieve(): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .get(baseApiUrl)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public delete(id: number): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .delete(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public create(entity: ISocialNetworkLink): Promise<ISocialNetworkLink> {
    return new Promise<ISocialNetworkLink>((resolve, reject) => {
      axios
        .post(`${baseApiUrl}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public update(entity: ISocialNetworkLink): Promise<ISocialNetworkLink> {
    return new Promise<ISocialNetworkLink>((resolve, reject) => {
      axios
        .put(`${baseApiUrl}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }
}
